    private ItemStack createBottleItem(String title, int reqLvl, int reqBooks, int currentBooks, Player player) {
        if (currentBooks < reqBooks) {
            ItemStack barrier = new ItemStack(Material.BARRIER); ItemMeta meta = barrier.getItemMeta();
            if (meta != null) {
                meta.setDisplayName("§c" + title + " §7(Bị Khóa)");
                meta.setLore(Arrays.asList("§7Yêu cầu cấu hình phòng tế:", " §8• §7Cần ít nhất: §e" + reqBooks + " Kệ Sách", " §8• §7Thực tế hiện có: §c" + currentBooks + "/" + reqBooks));
                barrier.setItemMeta(meta);
            }
            return barrier;
        }
        ItemStack bottle = new ItemStack(Material.EXPERIENCE_BOTTLE); ItemMeta meta = bottle.getItemMeta();
        if (meta != null) {
            meta.setDisplayName(title + " - §6" + reqLvl + " Levels");
            if (player.getLevel() < reqLvl) meta.setLore(Arrays.asList("§cMày đéo đủ cấp độ kinh nghiệm!"));
            else meta.setLore(Arrays.asList("§eClick chuột để tiến hành bùa chú!"));
            bottle.setItemMeta(meta);
        }
        return bottle;
    }

