    @EventHandler
    public void onPrepareGrindstone(PrepareGrindstoneEvent event) {
        ItemStack result = event.getResult();
        if (result == null || result.getType().isAir() || !result.hasItemMeta()) return;

        NamespacedKey flagKey = new NamespacedKey(plugin, "is_custom_enchanted");
        if (result.getItemMeta().getPersistentDataContainer().has(flagKey, PersistentDataType.INTEGER)) {
            ItemStack cleanResult = result.clone();
            ItemMeta meta = cleanResult.getItemMeta();

            // Thanh tẩy rễ NBT và bùa vanilla tương ứng
            meta.getPersistentDataContainer().remove(flagKey);
            for (CustomEnchant enc : CustomEnchant.values()) {
                meta.getPersistentDataContainer().remove(new NamespacedKey(plugin, "enchant_" + enc.getId()));
                org.bukkit.enchantments.Enchantment vanillaMatch = enchantManager.getVanillaEquivalent(enc);
                if (vanillaMatch != null) meta.removeEnchant(vanillaMatch);
            }
            try {
                meta.removeEnchant(org.bukkit.enchantments.Enchantment.getByKey(NamespacedKey.minecraft("luck_of_the_sea")));
                meta.removeEnchant(org.bukkit.enchantments.Enchantment.getByKey(NamespacedKey.minecraft("mending")));
            } catch (Throwable ignored) {}

            // 🎯 NỘI SOI PHẪU THUẬT LORE: Chỉ cắt đúng khối chữ của bùa chú, bảo toàn mọi text custom khác
            List<String> lore = meta.getLore();
            if (lore != null) {
                List<String> newLore = new ArrayList<>();
                for (String line : lore) {
                    String cleanedLine = line;
                    boolean isEnchantComponentRemoved = false;

                    for (CustomEnchant enc : CustomEnchant.values()) {
                        String regex = "§9" + java.util.regex.Pattern.quote(enc.getDisplayName()) + "(?: [IVX0-9]+)?";
                        String afterReplace = cleanedLine.replaceAll(regex, "");
                        if (!afterReplace.equals(cleanedLine)) {
                            cleanedLine = afterReplace;
                            isEnchantComponentRemoved = true;
                        }
                    }

                    if (isEnchantComponentRemoved) {
                        // Lau dọn sạch sẽ dấu phẩy ngăn cách thừa thãi
                        cleanedLine = cleanedLine.replaceAll("§7,\\s*", "").trim();
                        // Trút bỏ mã màu để kiểm nghiệm xem dòng này có chứa text thực tế không
                        String rawText = cleanedLine.replaceAll("(?i)§[0-9a-fk-or]", "").trim();
                        if (!rawText.isEmpty()) {
                            newLore.add(cleanedLine); // Nếu còn chỉ số / set bonus thì giữ lại
                        }
                    } else {
                        newLore.add(line); // Giữ nguyên vẹn 100% dòng không chứa bùa
                    }
                }
                meta.setLore(newLore);
            }
            cleanResult.setItemMeta(meta);
            event.setResult(cleanResult);
        }
    }

