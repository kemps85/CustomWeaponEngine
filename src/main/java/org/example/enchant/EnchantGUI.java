    @EventHandler
    public void onInventoryClose(InventoryCloseEvent event) {
        Player player = (Player) event.getPlayer();
        String title = event.getView().getTitle();
        if (title.equals(adminGuiName)) {
            adminGuiPlayers.remove(player.getUniqueId());
            return;
        }
        if (!title.equals(guiName)) return;
        Inventory gui = event.getInventory();
        activeBookshelves.remove(player.getUniqueId());
        ItemStack itemInTable = gui.getItem(13);
        if (itemInTable != null && itemInTable.getType() != Material.AIR) {
            for (ItemStack drop : player.getInventory().addItem(itemInTable).values()) {
                player.getWorld().dropItemNaturally(player.getLocation(), drop);
            }
        }
    }
