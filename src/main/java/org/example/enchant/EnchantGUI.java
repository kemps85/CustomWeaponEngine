    @EventHandler
    public void onOpenTable(PlayerInteractEvent event) {
        if (event.getAction() != Action.RIGHT_CLICK_BLOCK) return;
        Block block = event.getClickedBlock();
        if (block == null || block.getType() != Material.ENCHANTING_TABLE) return;

        event.setCancelled(true);
        Player player = event.getPlayer();

        int bookshelves = BookshelfScanner.countBookshelves(block);
        activeBookshelves.put(player.getUniqueId(), bookshelves);

        openCustomEnchantMenu(player);
    }

