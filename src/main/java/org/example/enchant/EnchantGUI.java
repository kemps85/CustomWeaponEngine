    @Override
    public boolean onCommand(CommandSender sender, Command command, String label, String[] args) {
        if (!(sender instanceof Player)) {
            sender.sendMessage("§cLệnh này chỉ dành cho người chơi!");
            return true;
        }
        Player player = (Player) sender;
        if (!player.isOp() && !player.hasPermission("cwe.admin")) {
            player.sendMessage("§c[CWE] Bạn không có quyền dùng lệnh này!");
            return true;
        }
        ItemStack mainHand = player.getInventory().getItemInMainHand();
        if (mainHand == null || mainHand.getType().isAir() || !isValidEnchantable(mainHand)) {
            player.sendMessage("§c[CWE] §fBạn phải đang §ecầm một trang bị hợp lệ §ftrên tay chính để dùng lệnh này!");
            player.playSound(player.getLocation(), org.bukkit.Sound.ENTITY_VILLAGER_NO, 1.0f, 1.0f);
            return true;
        }
        openAdminEnchantGUI(player);
        return true;
    }

    public void openAdminEnchantGUI(Player player) {
        Inventory gui = Bukkit.createInventory(null, 54, adminGuiName);
        // Fill background with dark purple glass
        ItemStack bgGlass = new ItemStack(Material.PURPLE_STAINED_GLASS_PANE);
        ItemMeta bgMeta = bgGlass.getItemMeta();
        if (bgMeta != null) { bgMeta.setDisplayName("§7 "); bgGlass.setItemMeta(bgMeta); }
        for (int i = 0; i < 54; i++) gui.setItem(i, bgGlass);

        // Populate enchant books — iterate ALL CustomEnchant values, fill slots 0..N
        int slot = 0;
        for (CustomEnchant enc : CustomEnchant.values()) {
            if (slot >= 54) break;
            ItemStack book = new ItemStack(Material.ENCHANTED_BOOK);
            ItemMeta meta = book.getItemMeta();
            if (meta != null) {
                meta.setDisplayName("§b✦ §l" + enc.getDisplayName());
                List<String> lore = new ArrayList<>();
                lore.add("§7Nhóm: §e" + enc.getItemGroup().name());
                lore.add("§7Max Cấp: §c" + enc.getMaxLevel());
                lore.add("§7" + enc.getDescription());
                lore.add("");
                lore.add("§a▶ Chuột Trái: §fCộng +1 Cấp vào trang bị");
                lore.add("§6▶ Chuột Phải: §fÉp thẳng Cấp TỐI ĐA (" + enc.getMaxLevel() + ")");
                meta.setLore(lore);
                // Add a glow effect
                meta.addEnchant(org.bukkit.enchantments.Enchantment.getByKey(NamespacedKey.minecraft("luck_of_the_sea")), 1, true);
                meta.addItemFlags(org.bukkit.inventory.ItemFlag.HIDE_ENCHANTS);
                // Store enchant ID in PDC so we can retrieve it on click
                meta.getPersistentDataContainer().set(
                    new NamespacedKey(plugin, "admin_enc_id"),
                    PersistentDataType.STRING,
                    enc.name()
                );
                book.setItemMeta(meta);
            }
            gui.setItem(slot, book);
            slot++;
        }
        
        // Populate Ultimate enchants
        for (UltimateEnchant uEnc : UltimateEnchant.values()) {
            if (slot >= 54) break;
            ItemStack book = new ItemStack(Material.ENCHANTED_BOOK);
            ItemMeta meta = book.getItemMeta();
            if (meta != null) {
                meta.setDisplayName("§d§l" + uEnc.getDisplayName().toUpperCase());
                List<String> lore = new ArrayList<>();
                lore.add("§7Nhóm: §dULTIMATE " + uEnc.getGroup().name());
                lore.add("§7Max Cấp: §c" + uEnc.getMaxLevel());
                lore.add("");
                lore.add("§a▶ Chuột Trái: §fCộng +1 Cấp vào trang bị");
                lore.add("§6▶ Chuột Phải: §fÉp thẳng Cấp TỐI ĐA (" + uEnc.getMaxLevel() + ")");
                meta.setLore(lore);
                meta.addEnchant(org.bukkit.enchantments.Enchantment.getByKey(NamespacedKey.minecraft("luck_of_the_sea")), 1, true);
                meta.addItemFlags(org.bukkit.inventory.ItemFlag.HIDE_ENCHANTS);
                meta.getPersistentDataContainer().set(
                    new NamespacedKey(plugin, "admin_ult_enc_id"),
                    PersistentDataType.STRING,
                    uEnc.name()
                );
                book.setItemMeta(meta);
            }
            gui.setItem(slot, book);
            slot++;
        }

        adminGuiPlayers.add(player.getUniqueId());
        player.openInventory(gui);
        player.sendMessage("§a[CWE Admin] §fMở giao diện ép bùa Admin. Chuột Trái §a+1 cấp§f, Chuột Phải §6Max Cấp§f.");
    }

