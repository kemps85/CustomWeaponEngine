    @EventHandler
    public void onAdminEnchantClick(InventoryClickEvent event) {
        if (!(event.getWhoClicked() instanceof Player)) return;
        Player player = (Player) event.getWhoClicked();
        // Only handle the admin GUI
        if (!event.getView().getTitle().equals(adminGuiName)) return;
        // Always cancel inventory manipulation in admin GUI
        event.setCancelled(true);

        // Only handle clicks on the TOP inventory (GUI itself), not bottom (player inventory)
        if (event.getClickedInventory() != event.getView().getTopInventory()) return;

        ItemStack clicked = event.getCurrentItem();
        if (clicked == null || clicked.getType() != Material.ENCHANTED_BOOK) return;
        if (!clicked.hasItemMeta()) return;

        // Read enchant ID from PDC
        
        String ultEncName = clicked.getItemMeta().getPersistentDataContainer().get(
            new NamespacedKey(plugin, "admin_ult_enc_id"),
            PersistentDataType.STRING
        );

        if (ultEncName != null) {
            handleAdminUltimateEnchantClick(player, ultEncName, event.getClick());
            return;
        }

        String encName = clicked.getItemMeta().getPersistentDataContainer().get(

            new NamespacedKey(plugin, "admin_enc_id"),
            PersistentDataType.STRING
        );
        if (encName == null) return;

        CustomEnchant enc;
        try {
            enc = CustomEnchant.valueOf(encName);
        } catch (IllegalArgumentException e) {
            player.sendMessage("§c[CWE Admin] Lỗi: Không tìm thấy bùa '" + encName + "'!");
            return;
        }

        // Get the item in admin's main hand
        ItemStack targetItem = player.getInventory().getItemInMainHand();
        if (targetItem == null || targetItem.getType().isAir() || !isValidEnchantable(targetItem)) {
            player.sendMessage("§c[CWE Admin] Bạn không còn cầm trang bị hợp lệ trên tay!");
            player.playSound(player.getLocation(), org.bukkit.Sound.ENTITY_VILLAGER_NO, 1.0f, 1.0f);
            return;
        }

        // ═══════════════════════════════════════════════════════
        // BẮT BUỘC kiểm tra ItemGroup.canApply - CHẶN ÉP SAI NHÓM
        // ═══════════════════════════════════════════════════════
        Material mat = targetItem.getType();
        if (!enc.getItemGroup().canApply(mat)) {
            player.sendMessage("§c[CWE Admin] §4❌ CHẶN! §fBùa §e" + enc.getDisplayName() +
                " §f(nhóm §c" + enc.getItemGroup().name() + "§f) KHÔNG THỂ ép lên §e" +
                mat.name() + "§f! Hủy lệnh.");
            player.playSound(player.getLocation(), org.bukkit.Sound.ENTITY_VILLAGER_NO, 1.0f, 1.2f);
            return;
        }

        // Additional slot restriction checks (same logic as EnchantGUI)
        String matName = mat.name();
        if (enc == CustomEnchant.BIG_BRAIN && !matName.contains("HELMET")) {
            player.sendMessage("§c[CWE Admin] Big Brain chỉ ép được vào Mũ!"); return;
        }
        if (enc == CustomEnchant.COUNTER_STRIKE && !matName.contains("CHESTPLATE")) {
            player.sendMessage("§c[CWE Admin] Counter-Strike chỉ ép được vào Áo Giáp!"); return;
        }
        if (enc == CustomEnchant.SMARTY_PANTS && !matName.contains("LEGGINGS")) {
            player.sendMessage("§c[CWE Admin] Smarty Pants chỉ ép được vào Quần!"); return;
        }
        if (enc == CustomEnchant.SUGAR_RUSH && !matName.contains("BOOTS")) {
            player.sendMessage("§c[CWE Admin] Sugar Rush chỉ ép được vào Giày!"); return;
        }
        if (enc == CustomEnchant.FEATHER_FALLING && !matName.contains("BOOTS")) {
            player.sendMessage("§c[CWE Admin] Feather Falling chỉ ép được vào Giày!"); return;
        }
        if (enc == CustomEnchant.REPLENISH && !matName.contains("HOE")) {
            player.sendMessage("§c[CWE Admin] Replenish chỉ ép được vào Cuốc!"); return;
        }

        boolean isLeft = event.getClick() == org.bukkit.event.inventory.ClickType.LEFT;
        boolean isRight = event.getClick() == org.bukkit.event.inventory.ClickType.RIGHT;
        if (!isLeft && !isRight) return;

        // Get current level of enchant on item
        NamespacedKey encKey = new NamespacedKey(plugin, "enchant_" + enc.getId());
        int currentLevel = 0;
        if (targetItem.hasItemMeta()) {
            PersistentDataContainer pdc = targetItem.getItemMeta().getPersistentDataContainer();
            if (pdc.has(encKey, PersistentDataType.INTEGER)) {
                currentLevel = pdc.get(encKey, PersistentDataType.INTEGER);
            }
        }

        int newLevel;
        if (isLeft) {
            // Left Click: +1 level, capped at maxLevel
            newLevel = Math.min(currentLevel + 1, enc.getMaxLevel());
            if (newLevel == currentLevel) {
                player.sendMessage("§e[CWE Admin] §fBùa §b" + enc.getDisplayName() + " §fđã đạt §cCấp TỐI ĐA (" + enc.getMaxLevel() + ")§f rồi!");
                player.playSound(player.getLocation(), org.bukkit.Sound.ENTITY_VILLAGER_NO, 1.0f, 1.0f);
                return;
            }
        } else {
            // Right Click: set to maxLevel directly
            newLevel = enc.getMaxLevel();
        }

        // Apply enchant
        enchantManager.applyEnchantToItem(targetItem, enc, newLevel);
        // Force lore rebuild
        enchantManager.rebuildEnchantLore(targetItem);
        // Update item in hand (Bukkit requires setItemInMainHand to sync)
        player.getInventory().setItemInMainHand(targetItem);
        player.updateInventory();

        String action = isLeft ? "§a+1 Cấp → §fCấp §e" + newLevel : "§6Max Cấp → §fCấp §e" + newLevel;
        player.sendMessage("§a[CWE Admin] ✔ §b" + enc.getDisplayName() + " §f| " + action + " §f| §7(" + mat.name() + ")");
        player.playSound(player.getLocation(), org.bukkit.Sound.BLOCK_ENCHANTMENT_TABLE_USE, 1.0f, 1.5f);
        // GUI stays open — do NOT close it!
    }

