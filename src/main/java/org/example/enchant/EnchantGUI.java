    @EventHandler
    public void onInventoryClick(InventoryClickEvent event) {
        if (!event.getView().getTitle().equals(guiName)) return;

        Player player = (Player) event.getWhoClicked();
        Inventory gui = event.getInventory();
        int slot = event.getSlot();

        if (event.getClick().isShiftClick()) { event.setCancelled(true); return; }
        if (event.getClickedInventory() != event.getView().getTopInventory()) {
            Bukkit.getScheduler().runTask(plugin, () -> refreshEnchantButtons(gui, player));
            return;
        }

        if (slot != 13 && slot != 29 && slot != 31 && slot != 33) { event.setCancelled(true); return; }
        if (slot == 13) { Bukkit.getScheduler().runTask(plugin, () -> refreshEnchantButtons(gui, player)); return; }

        if (slot == 29 || slot == 31 || slot == 33) {
            event.setCancelled(true);
            ItemStack targetItem = gui.getItem(13);

            if (targetItem == null || targetItem.getType().isAir()) {
                player.sendMessage("§cĐã có đồ đéo đâu mà đòi phù phép cu m!");
                return;
            }

            if (!isValidEnchantable(targetItem)) {
                player.sendMessage("§c[Engine] Vật phẩm không hợp lệ! Bàn enchant chỉ nhận Vũ khí, Công cụ, Giáp, Wand hoặc Sách thường.");
                player.playSound(player.getLocation(), org.bukkit.Sound.ENTITY_VILLAGER_NO, 1.0f, 1.0f);
                return;
            }

            PersistentDataContainer container = targetItem.getItemMeta().getPersistentDataContainer();
            NamespacedKey flagKey = new NamespacedKey(plugin, "is_custom_enchanted");
            if (container.has(flagKey, PersistentDataType.INTEGER)) {
                player.sendMessage("§cMón đồ này đã được khai quang rồi, mang ra cái Đe mà gộp dòng đi!");
                player.playSound(player.getLocation(), org.bukkit.Sound.ENTITY_VILLAGER_NO, 1.0f, 1.0f);
                return;
            }

            int bookshelves = activeBookshelves.getOrDefault(player.getUniqueId(), 0);
            int reqLevel = 0; int reqBookshelves = 0; int tier = 0;

            if (slot == 29) { tier = 1; reqLevel = 5; reqBookshelves = 0; }
            else if (slot == 31) { tier = 2; reqLevel = 15; reqBookshelves = 5; }
            else if (slot == 33) { tier = 3; reqLevel = 35; reqBookshelves = 15; }

            if (bookshelves < reqBookshelves) { player.sendMessage("§cKệ sách không đủ!"); return; }
            if (player.getLevel() < reqLevel) { player.sendMessage("§cKhông đủ level!"); return; }

            boolean success = false;
            List<CustomEnchant> pool = new ArrayList<>();
            Material mat = targetItem.getType();
            String name = mat.name();
            boolean isAxe = (name.endsWith("_AXE") || name.equals("AXE")) && !name.contains("PICKAXE");
            boolean axeChooseSword = Math.random() < 0.5;

            for (CustomEnchant enc : CustomEnchant.values()) {
                if (enc == CustomEnchant.MENDING) continue;

                if (mat == Material.BOOK) { pool.add(enc); continue; }
                if (isAxe) {
                    if (axeChooseSword && enc.getItemGroup() == CustomEnchant.ItemGroup.SWORD) pool.add(enc);
                    else if (!axeChooseSword && enc.getItemGroup() == CustomEnchant.ItemGroup.TOOL) pool.add(enc);
                    continue;
                }
                if (enc.getItemGroup().canApply(mat)) {
                    if (enc == CustomEnchant.BIG_BRAIN && !name.contains("HELMET")) continue;
                    if (enc == CustomEnchant.COUNTER_STRIKE && !name.contains("CHESTPLATE")) continue;
                    if (enc == CustomEnchant.SMARTY_PANTS && !name.contains("LEGGINGS")) continue;
                    if (enc == CustomEnchant.SUGAR_RUSH && !name.contains("BOOTS")) continue;
                    if (enc == CustomEnchant.FEATHER_FALLING && !name.contains("BOOTS")) continue;
                    if (enc == CustomEnchant.REPLENISH && !name.contains("HOE")) continue;
                    pool.add(enc);
                }
            }

            if (!pool.isEmpty()) {
                Collections.shuffle(pool);
                int countToApply = Math.min(2 + (int) (Math.random() * 2), pool.size());
                for (int i = 0; i < countToApply; i++) {
                    CustomEnchant randomEnc = pool.get(i);
                    int lvl = 1;
                    if (tier == 1) lvl = Math.max(1, randomEnc.getMaxLevel() / 3);
                    else if (tier == 2) lvl = Math.max(1, (randomEnc.getMaxLevel() * 2) / 3);
                    else if (tier == 3) lvl = randomEnc.getMaxLevel();

                    if (enchantManager.applyEnchantToItem(targetItem, randomEnc, lvl)) success = true;
                }
            }

            if (success) {
                if (targetItem.getType() == Material.BOOK) {
                    targetItem.setType(Material.ENCHANTED_BOOK);
                    enchantManager.rebuildEnchantLore(targetItem);
                }
                player.setLevel(player.getLevel() - reqLevel);
                player.sendMessage("§a🟩 Phù phép thành công! Đã khấu trừ §e" + reqLevel + " Levels§a.");
                player.playSound(player.getLocation(), org.bukkit.Sound.BLOCK_ENCHANTMENT_TABLE_USE, 1.0f, 1.0f);
                refreshEnchantButtons(gui, player);
            }
        }
    }

