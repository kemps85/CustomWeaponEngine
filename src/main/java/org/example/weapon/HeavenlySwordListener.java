    private final HashMap<UUID, HashMap<String, Long>> globalCooldowns = new HashMap<>();

    public void clearCooldowns() { this.globalCooldowns.clear(); }

    private String getWeaponIdFromDisplayName(String displayName) {
        FileConfiguration config = CustomWeaponEngine.getInstance().getConfig();
        if (!config.contains("weapons")) return null;
        for (String id : config.getConfigurationSection("weapons").getKeys(false)) {
            String configName = config.getString("weapons." + id + ".display-name");
            if (configName == null) continue;
            String cleanName = ChatColor.stripColor(ChatColor.translateAlternateColorCodes('&', configName));
            if (ChatColor.stripColor(displayName).contains(cleanName)) return id;
        }
        return null;
    }

    private double calculateFinalManaCost(Player player, double baseManaCost) {
        double totalReduction = ArmorManager.getGlobalManaReduction(player);
        return baseManaCost * (1.0 - (totalReduction / 100.0));
    }

    public long getRemainingCooldown(UUID playerUuid, String weaponId) {
        if (!globalCooldowns.containsKey(playerUuid)) return 0;
        HashMap<String, Long> playerCds = globalCooldowns.get(playerUuid);
        if (!playerCds.containsKey(weaponId)) return 0;

        long lastUsed = playerCds.get(weaponId);
        FileConfiguration config = CustomWeaponEngine.getInstance().getConfig();
        int cooldownSec = config.getInt("weapons." + weaponId + ".cooldown", 0);

        Player player = Bukkit.getPlayer(playerUuid);
        double cdReduction = (player != null) ? ArmorManager.getGlobalCooldownReduction(player) : 0.0;
        long finalCooldownMillis = (long) ((cooldownSec * 1000L) * (1.0 - (cdReduction / 100.0)));

        long timePassed = System.currentTimeMillis() - lastUsed;
        long timeLeft = finalCooldownMillis - timePassed;

        return (timeLeft <= 0) ? 0 : (timeLeft / 1000 + 1);
    }

    @EventHandler
    public void onWeaponRightClick(PlayerInteractEvent event) {
        Player player = event.getPlayer();
        if (event.getAction() != Action.RIGHT_CLICK_AIR && event.getAction() != Action.RIGHT_CLICK_BLOCK) return;

        ItemStack item = player.getInventory().getItemInMainHand();
        if (item == null || !item.hasItemMeta() || !item.getItemMeta().hasDisplayName()) return;

        // 🟩 CHẶN GIÁP CƯỚP CHIÊU VŨ KHÍ: Nếu đang cầm giáp trên tay để mặc, cấm tuyệt đối xả chiêu kiếm!
        String typeName = item.getType().name();
        if (typeName.contains("HELMET") || typeName.contains("CHESTPLATE") || typeName.contains("LEGGINGS") || typeName.contains("BOOTS")) {
            return; 
        }

        String weaponId = getWeaponIdFromDisplayName(item.getItemMeta().getDisplayName());
        if (weaponId == null) return;

        FileConfiguration config = CustomWeaponEngine.getInstance().getConfig();
        int cooldownSec = config.getInt("weapons." + weaponId + ".cooldown", 0);
        String effectType = config.getString("weapons." + weaponId + ".effect", "NONE");
        double baseManaCost = config.getDouble("weapons." + weaponId + ".mana-cost", 0.0);

        double finalManaCost = calculateFinalManaCost(player, baseManaCost);
        long currentTime = System.currentTimeMillis();
        globalCooldowns.putIfAbsent(player.getUniqueId(), new HashMap<>());
        HashMap<String, Long> playerCds = globalCooldowns.get(player.getUniqueId());

        double cdReduction = ArmorManager.getGlobalCooldownReduction(player);
        long finalCooldownMillis = (long) ((cooldownSec * 1000L) * (1.0 - (cdReduction / 100.0)));

        if (playerCds.containsKey(weaponId) && (currentTime - playerCds.get(weaponId) < finalCooldownMillis)) {
            player.sendMessage("§c§l[System] §cThis skill is currently on cooldown!");
            return;
        }

        SkillsUser user = AuraSkillsApi.get().getUser(player.getUniqueId());
        if (user != null && finalManaCost > 0) {
            if (user.getMana() < finalManaCost) {
                player.sendMessage("§c§l[System] §cNot enough Mana! Required: " + (int)finalManaCost + " Mana.");
                return;
            }
        }

        // Mechanical 1: DASH
        if (effectType.equalsIgnoreCase("DASH")) {
            double dashForce = config.getDouble("weapons." + weaponId + ".dash-force", 1.4);
            
            Vector direction = player.getLocation().getDirection();
            direction.setY(0.2); 
            player.setVelocity(direction.multiply(dashForce));
            
            player.getWorld().playSound(player.getLocation(), Sound.ENTITY_PLAYER_ATTACK_SWEEP, 1.0f, 1.2f);
            player.getWorld().spawnParticle(Particle.CLOUD, player.getLocation(), 8, 0.2, 0.2, 0.2, 0.1);

            if (user != null && finalManaCost > 0) user.setMana(user.getMana() - finalManaCost);
            playerCds.put(weaponId, currentTime);
        }

        // Mechanical 2: MAGIC_PROJECTILE
        else if (effectType.equalsIgnoreCase("MAGIC_PROJECTILE")) {
            double skillDamage = config.getDouble("weapons." + weaponId + ".skill-damage", 10.0);
            double maxRange = config.getDouble("weapons." + weaponId + ".range", 12.0);
            String element = config.getString("weapons." + weaponId + ".element", "NORMAL").toUpperCase();

            Location startLocation = player.getEyeLocation();
            Vector direction = startLocation.getDirection().normalize();

            Sound castSound = Sound.ENTITY_ILLUSIONER_CAST_SPELL;
            if (element.equals("FIRE")) castSound = Sound.ENTITY_BLAZE_SHOOT;
            else if (element.equals("ICE")) castSound = Sound.ENTITY_SNOWBALL_THROW;
            else if (element.equals("LIGHTNING")) castSound = Sound.ENTITY_LIGHTNING_BOLT_THUNDER;

            player.getWorld().playSound(player.getLocation(), castSound, 1.0f, 1.3f);

            new BukkitRunnable() {
                private double blocksTraveled = 0.0;
                private final Location currentLoc = startLocation.clone();
                private final int stepsPerTick = 6;
                private final double stepSize = 0.5;

                @Override
                public void run() {
                    for (int i = 0; i < stepsPerTick; i++) {
                        currentLoc.add(direction.clone().multiply(stepSize));
                        blocksTraveled += stepSize;

                        switch (element) {
                            case "FIRE":
                                currentLoc.getWorld().spawnParticle(Particle.FLAME, currentLoc, 1, 0, 0, 0, 0);
                                break;
                            case "ICE":
                                currentLoc.getWorld().spawnParticle(Particle.SNOWFLAKE, currentLoc, 1, 0, 0, 0, 0);
                                break;
                            case "LIGHTNING":
                                currentLoc.getWorld().spawnParticle(Particle.ENCHANTED_HIT, currentLoc, 1, 0, 0, 0, 0);
                                break;
                            default:
                                currentLoc.getWorld().spawnParticle(Particle.END_ROD, currentLoc, 1, 0, 0, 0, 0);
                                currentLoc.getWorld().spawnParticle(Particle.WITCH, currentLoc, 1, 0.02, 0.02, 0.02, 0.01);
                                break;
                        }

                        if (currentLoc.getBlock().getType().isSolid()) {
                            cancel(); return;
                        }

                        for (Entity entity : currentLoc.getWorld().getNearbyEntities(currentLoc, 0.4, 0.4, 0.4)) {
                            if (entity instanceof LivingEntity && !(entity instanceof Player)) {
                                LivingEntity targetMob = (LivingEntity) entity;
                                targetMob.damage(skillDamage, player);
                                
                                if (element.equals("FIRE")) {
                                    targetMob.setFireTicks(60); 
                                    targetMob.getWorld().spawnParticle(Particle.LAVA, currentLoc, 4, 0.1, 0.1, 0.1, 0.1);
                                } else if (element.equals("ICE")) {
                                    targetMob.addPotionEffect(new PotionEffect(PotionEffectType.SLOWNESS, 60, 1)); 
                                } else if (element.equals("LIGHTNING")) {
                                    targetMob.getWorld().strikeLightningEffect(targetMob.getLocation()); 
                                } else {
                                    targetMob.getWorld().spawnParticle(Particle.EXPLOSION, currentLoc, 3, 0.1, 0.1, 0.1, 0.1);
                                }
                                
                                targetMob.getWorld().playSound(currentLoc, Sound.ENTITY_ZOMBIE_VILLAGER_CURE, 0.8f, 1.6f);
                                cancel(); return;
                            }
                        }

                        if (blocksTraveled >= maxRange) {
                            cancel(); return;
                        }
                    }
                }
            }.runTaskTimer(CustomWeaponEngine.getInstance(), 0L, 1L);

            if (user != null && finalManaCost > 0) user.setMana(user.getMana() - finalManaCost);
            playerCds.put(weaponId, currentTime);
        }

        // Mechanical 3: EXPLOSION
        else if (effectType.equalsIgnoreCase("EXPLOSION")) {
            double skillDamage = config.getDouble("weapons." + weaponId + ".skill-damage", 15.0);
            
            if (player.getTargetEntity(7) instanceof LivingEntity) {
                LivingEntity targetMob = (LivingEntity) player.getTargetEntity(7);
                if (targetMob instanceof Player) return;

                targetMob.getWorld().spawnParticle(Particle.EXPLOSION_EMITTER, targetMob.getLocation(), 1);
                targetMob.getWorld().playSound(targetMob.getLocation(), Sound.ENTITY_GENERIC_EXPLODE, 1.0f, 1.0f);
                
                targetMob.damage(skillDamage, player);
                targetMob.setFireTicks(40); 

                if (user != null && finalManaCost > 0) user.setMana(user.getMana() - finalManaCost);
                playerCds.put(weaponId, currentTime);
            }
        }
    }

    @EventHandler
    public void onWeaponPassive(EntityDamageByEntityEvent event) {
        if (!(event.getDamager() instanceof Player) || !(event.getEntity() instanceof LivingEntity)) return;

        Player player = (Player) event.getDamager();
        LivingEntity mob = (LivingEntity) event.getEntity();
        if (mob instanceof Player) return; 

        ItemStack item = player.getInventory().getItemInMainHand();
        if (item == null || !item.hasItemMeta() || !item.getItemMeta().hasDisplayName()) return;

        String weaponId = getWeaponIdFromDisplayName(item.getItemMeta().getDisplayName());
        if (weaponId == null) return;

        FileConfiguration config = CustomWeaponEngine.getInstance().getConfig();
        
        if (config.contains("weapons." + weaponId + ".execute-threshold")) {
            double execThreshold = config.getDouble("weapons." + weaponId + ".execute-threshold");
            AttributeInstance maxHealthAttr = mob.getAttribute(Attribute.GENERIC_MAX_HEALTH);
            if (maxHealthAttr != null) {
                double remainingHealth = mob.getHealth() - event.getFinalDamage();
                if (remainingHealth > 0 && (remainingHealth / maxHealthAttr.getValue()) < (execThreshold / 100.0)) {
                    event.setDamage(999999); 
                    player.sendMessage("§4§l[EXECUTE] §cTarget eliminated by weapon passive!");
                }
            }
        }
    }
