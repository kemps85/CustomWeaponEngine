public class ReplenishListener implements Listener, CommandExecutor {
    private final JavaPlugin plugin;
    private final String metaKey = "cst_sapling_protection";
    private final Map<UUID, Boolean> protectionToggle = new HashMap<>();

    public ReplenishListener(JavaPlugin plugin) {
        this.plugin = plugin;
    }

    @Override
    public boolean onCommand(CommandSender sender, Command command, String label, String[] args) {
        if (!(sender instanceof Player)) return true;
        Player player = (Player) sender;
        UUID uuid = player.getUniqueId();
        boolean nextStatus = !protectionToggle.getOrDefault(uuid, true);
        protectionToggle.put(uuid, nextStatus);
        player.sendMessage(nextStatus ? "§a🟩 [Replenish] Đã BẬT bảo vệ cây non." : "§c🟥 [Replenish] Đã TẮT bảo vệ cây non.");
        return true;
    }

    @EventHandler(priority = EventPriority.HIGHEST, ignoreCancelled = true)
    public void onBlockBreak(BlockBreakEvent event) {
        Player player = event.getPlayer();
        Block block = event.getBlock();
        ItemStack tool = player.getInventory().getItemInMainHand();
        UUID uuid = player.getUniqueId();
        EnchantManager em = CustomWeaponEngine.getEnchantManager();

        boolean hasReplenish = false;
        if (tool != null && tool.hasItemMeta()) {
            NamespacedKey key = new NamespacedKey(plugin, "enchant_replenish");
            if (tool.getItemMeta().getPersistentDataContainer().has(key, PersistentDataType.INTEGER)) {
                if (em.isChargeable(CustomEnchant.REPLENISH)) {
                    if (em.getCharges(tool, CustomEnchant.REPLENISH) > 0) {
                        hasReplenish = true;
                    } else {
                        player.sendActionBar("§c§l⚡ Bùa Replenish đã hết pin! Hãy sạc bằng Hạt Giống trên Đe.");
                    }
                } else {
                    hasReplenish = true;
                }
            }
        }
        
        boolean isProtectionEnabled = protectionToggle.getOrDefault(uuid, true);

        if (hasReplenish && isProtectionEnabled && block.hasMetadata(metaKey)) {
            BlockData data = block.getBlockData();
            if (data instanceof Ageable && ((Ageable) data).getAge() == 0) {
                long protectionTime = 0;
                boolean alreadyWarned = false;
                for (MetadataValue value : block.getMetadata(metaKey)) {
                    if (value.getOwningPlugin().equals(plugin)) {
                        String[] split = value.asString().split(";");
                        protectionTime = Long.parseLong(split[0]);
                        alreadyWarned = Boolean.parseBoolean(split[1]);
                        break;
                    }
                }

                if (System.currentTimeMillis() < protectionTime) {
                    if (!alreadyWarned) {
                        event.setCancelled(true);
                        block.setMetadata(metaKey, new FixedMetadataValue(plugin, protectionTime + ";true"));
                        player.sendActionBar("§c§l🛡 Bảo vệ cây non! Đập thêm lần nữa để phá.");
                        player.playSound(player.getLocation(), org.bukkit.Sound.ENTITY_ITEM_BREAK, 0.4f, 1.5f);
                        return;
                    }
                }
            }
        }

        if (!hasReplenish) {
            block.removeMetadata(metaKey, plugin);
        }

        NamespacedKey repKey = new NamespacedKey(plugin, "enchant_replenish");
        NamespacedKey teleKey = new NamespacedKey(plugin, "enchant_telekinesis");

        if (hasReplenish) {
            BlockData blockData = block.getBlockData();
            if (blockData instanceof Ageable && ((Ageable) blockData).getAge() == ((Ageable) blockData).getMaximumAge()) {
                Material cropMaterial = block.getType();
                Material seedNeeded = getSeedForCrop(cropMaterial);
                if (seedNeeded != null) {
                    final BlockData replantData = blockData.clone();
                    ((Ageable) replantData).setAge(0);
                    Bukkit.getScheduler().runTaskLater(plugin, () -> {
                        if (consumeSeed(player, seedNeeded)) {
                            block.setType(cropMaterial);
                            block.setBlockData(replantData);
                            long expireTime = System.currentTimeMillis() + 3000;
                            block.setMetadata(metaKey, new FixedMetadataValue(plugin, expireTime + ";false"));
                            
                            // Tiêu hao pin và cập nhật lore báu vật
                            em.consumeCharge(tool, CustomEnchant.REPLENISH, 1);
                            em.rebuildEnchantLore(tool);
                        }
                    }, 1L);
                }
            }
        }

        if (tool != null && tool.hasItemMeta() && tool.getItemMeta().getPersistentDataContainer().has(teleKey, PersistentDataType.INTEGER)) {
            // Kiểm tra pin bùa Telekinesis trước khi hút
            if (em.getCharges(tool, CustomEnchant.TELEKINESIS) > 0) {
                Location loc = block.getLocation().add(0.5, 0.5, 0.5);
                Bukkit.getScheduler().runTaskLater(plugin, () -> {
                    boolean addedAny = false;
                    for (Entity entity : loc.getWorld().getNearbyEntities(loc, 2.5, 2.5, 2.5)) {
                        if (entity instanceof Item && ((Item) entity).getTicksLived() <= 2) {
                            Map<Integer, ItemStack> leftover = player.getInventory().addItem(((Item) entity).getItemStack());
                            if (leftover.isEmpty()) { entity.remove(); addedAny = true; }
                            else { ((Item) entity).setItemStack(leftover.get(0)); addedAny = true; }
                        }
                    }
                    if (addedAny) {
                        player.updateInventory();
                        em.consumeCharge(tool, CustomEnchant.TELEKINESIS, 1);
                        em.rebuildEnchantLore(tool);
                    }
                }, 1L);
            } else {
                player.sendActionBar("§c§l⚡ Bùa Telekinesis đã hết pin! Hãy sạc bằng Ngọc Ender trên Đe.");
            }
        }
    }

    private Material getSeedForCrop(Material crop) {
        switch (crop) {
            case WHEAT: return Material.WHEAT_SEEDS;
            case CARROTS: return Material.CARROT;
            case POTATOES: return Material.POTATO;
            case BEETROOTS: return Material.BEETROOT_SEEDS;
            case NETHER_WART: return Material.NETHER_WART;
            case COCOA: return Material.COCOA_BEANS;
            default: return null;
        }
    }

    private boolean consumeSeed(Player player, Material seed) {
        for (ItemStack item : player.getInventory().getContents()) {
            if (item != null && item.getType() == seed) {
                item.setAmount(item.getAmount() - 1);
                return true;
            }
        }
        return false;
    }
}

